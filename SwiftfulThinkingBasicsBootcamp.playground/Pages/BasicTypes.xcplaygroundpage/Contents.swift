import Foundation

// String is "regular text"
let myFirstItem: String = "Hello, world!"

// Reference previously created objects
let myTitle = myFirstItem

// Boolean (aka Bool) is true or false
let mySecondItem: Bool = true
let myThirdItem: Bool = false

// Do not do!
// let myFourthItem: String = "true"

// Swift is a type-safe language, once a type is set, it cannot be changed like it can be in Ruby, Python & Javascript
//let myFifthItem: Bool = "Hello, world!" // This wont compile and give an error
let mySixthItem: String = "Hello, World!"
let mySeventhItem = "Hello, World!"

// Date
let myFirstDate: Date = Date()

// Numbers can be Int, Double, CGFloat & more
// `Int` is a whole number like 1,2,3,4,5
let myFirstNumber: Int = 1

// `Double` is a number with decimal like 1.2, 3.234, 5.0
let mySecondNumber: Double = 1.0

// `CGFloat` is also like number but used mainly for UI components in iOS
let myThirdNumber: CGFloat = 1.0
